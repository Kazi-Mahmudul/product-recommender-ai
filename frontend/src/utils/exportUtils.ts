/**
 * Generate shareable URL for comparison
 */
export function generateShareableUrl(phoneIds: number[], baseUrl: string = window.location.origin): string {
  const path = `/compare/${phoneIds.join('-')}`;
  return `${baseUrl}${path}`;
}

/**
 * Copy text to clipboard
 */
export async function copyToClipboard(text: string): Promise<boolean> {
  try {
    if (navigator.clipboard && window.isSecureContext) {
      await navigator.clipboard.writeText(text);
      return true;
    } else {
      // Fallback for older browsers
      const textArea = document.createElement('textarea');
      textArea.value = text;
      textArea.style.position = 'fixed';
      textArea.style.left = '-999999px';
      textArea.style.top = '-999999px';
      document.body.appendChild(textArea);
      textArea.focus();
      textArea.select();
      
      const result = document.execCommand('copy');
      document.body.removeChild(textArea);
      return result;
    }
  } catch (error) {
    console.error('Failed to copy to clipboard:', error);
    return false;
  }
}

/**
 * Share comparison via Web Share API (if supported)
 */
export async function shareComparison(phones: Phone[], url: string): Promise<boolean> {
  if (!navigator.share) {
    return false;
  }

  try {
    const phoneNames = phones.map(p => `${p.brand} ${p.name}`).join(' vs ');
    
    await navigator.share({
      title: `Phone Comparison: ${phoneNames}`,
      text: `Check out this phone comparison on Epick: ${phoneNames}`,
      url: url
    });
    
    return true;
  } catch (error) {
    console.error('Error sharing:', error);
    return false;
  }
}

/**
 * Comparison history management
 */
export interface ComparisonHistoryItem {
  id: string;
  phoneIds: number[];
  phoneNames: string[];
  timestamp: Date;
  url: string;
}

const HISTORY_STORAGE_KEY = 'epick_comparison_history';
const MAX_HISTORY_ITEMS = 10;

/**
 * Save comparison to history
 */
export function saveComparisonToHistory(phones: Phone[]): void {
  try {
    const historyItem: ComparisonHistoryItem = {
      id: Date.now().toString(),
      phoneIds: phones.map(p => p.id),
      phoneNames: phones.map(p => `${p.brand} ${p.name}`),
      timestamp: new Date(),
      url: generateShareableUrl(phones.map(p => p.id))
    };

    const existingHistory = getComparisonHistory();
    
    // Remove duplicate if exists (same phone combination)
    const filteredHistory = existingHistory.filter(item => 
      !arraysEqual(item.phoneIds.sort(), historyItem.phoneIds.sort())
    );

    // Add new item to beginning
    const newHistory = [historyItem, ...filteredHistory].slice(0, MAX_HISTORY_ITEMS);
    
    localStorage.setItem(HISTORY_STORAGE_KEY, JSON.stringify(newHistory));
  } catch (error) {
    console.error('Error saving comparison to history:', error);
  }
}

/**
 * Get comparison history
 */
export function getComparisonHistory(): ComparisonHistoryItem[] {
  try {
    const historyJson = localStorage.getItem(HISTORY_STORAGE_KEY);
    if (!historyJson) return [];

    const history = JSON.parse(historyJson);
    
    // Convert timestamp strings back to Date objects
    return history.map((item: any) => ({
      ...item,
      timestamp: new Date(item.timestamp)
    }));
  } catch (error) {
    console.error('Error loading comparison history:', error);
    return [];
  }
}

/**
 * Clear comparison history
 */
export function clearComparisonHistory(): void {
  try {
    localStorage.removeItem(HISTORY_STORAGE_KEY);
  } catch (error) {
    console.error('Error clearing comparison history:', error);
  }
}

/**
 * Helper function to compare arrays
 */
function arraysEqual(a: number[], b: number[]): boolean {
  if (a.length !== b.length) return false;
  return a.every((val, index) => val === b[index]);
}