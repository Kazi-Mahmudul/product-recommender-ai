/**
 * Generate PDF content for phone comparison
 * Note: This is a simplified implementation. For production, consider using libraries like jsPDF or Puppeteer
 */
export function generateComparisonPDF(phones: Phone[], verdict?: string): Promise<void> {
  return new Promise((resolve, reject) => {
    try {
      // Create a new window with the comparison content
      const printWindow = window.open('', '_blank');
      
      if (!printWindow) {
        throw new Error('Unable to open print window. Please check your popup blocker settings.');
      }

      const htmlContent = generatePrintableHTML(phones, verdict);
      
      printWindow.document.write(htmlContent);
      printWindow.document.close();
      
      // Wait for content to load, then trigger print
      printWindow.onload = () => {
        setTimeout(() => {
          printWindow.print();
          printWindow.close();
          resolve();
        }, 500);
      };
      
    } catch (error) {
      console.error('Error generating PDF:', error);
      reject(error);
    }
  });
}

